package com.sercomm.commons.umei;

public class UMEiError
{
    private int code;
    private String detail;
    
    public int getCode()
    {
        return this.code;
    }
    
    public void setCode(int code)
    {
        this.code = code;
    }
    
    public String getDetail()
    {
        return this.detail;
    }
    
    public void setDetail(String detail)
    {
        this.detail = detail;
    }
}
